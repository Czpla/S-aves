    private void showAviary() {
        List<Aviary> aviarys = this.persistAviary.show();
        
        if(!aviarys.isEmpty()) {
            for(Aviary aviary : aviarys) {
                model.addRow(new Object[]{
                    aviary.getName(), aviary.getCity(), 
                    aviary.getState(), aviary.getStreet(), 
                    aviary.getNumber(), aviary.getSize()
                });
            }
        }
    }

